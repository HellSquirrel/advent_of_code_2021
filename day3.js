const brackets = {
  "}": "{",
  ")": "(",
  ">": "<",
  "]": "[",
};

const score = {
  "}": 3,
  ")": 1,
  ">": 4,
  "]": 2,
};

const analyzeLines = (line) => {
  let chars = [];
  let error = null;
  for (const char of line) {
    const openingBracket = brackets[char];
    if (openingBracket) {
      const [current, ...remaining] = chars;
      if (current !== openingBracket) {
        error = char;
        break;
      } else {
        chars = remaining;
      }
    } else if (Object.values(brackets).includes(char)) {
      chars = [char, ...chars];
    }
  }

  return { error, chars, line };
};

const lines = input
  .split("\n")
  .map((e) => e.trim())
  .map((e) => e.split(""))
  .map(analyzeLines)
  .filter((e) => !e.error)
  .map(e => e.chars
    .map(ch => Object.entries(brackets).find(b => b[1] === ch)[0])
    .reduce((a,v) => 5 * a +  score[v], 0)
    )
  .sort((a,b) => a > b ? -1 : 1)

